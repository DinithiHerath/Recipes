          <b>Preparation</b>
          <ol style="text-align: justify;">
            <li>Clean the chicken breasts and dice them.</li>
            <li>Stir together the cooking wine, cooking starch, and salt, and marinated the chicken cubes in the mixture for half an hour.</li>
            <li>Soak peanuts in hot water for 10 minutes and remove the peanut skins.</li>
            <li>Clean the dried red chili peppers and cut them into small pieces. Clean and chop the green onions. Clean and dice the ginger and garlic.</li><br>
