            <h2><u>Chapati</u></h2><br>
            
            <b>Main ingredients:</b>
            <ul>
             <li>2 cups whole wheat flour</li>
            <li>1 teaspoon salt</li>
            <li>1 tablespoon oil</li>
            <li>Warm water (as needed)</li>
          </ul><br>
              
            <b>Cooking Instructions</b>
            <ol style="text-align: justify;">
              <li>In a mixing bowl, combine the whole wheat flour and salt. </li>
              <li>Add the oil and mix it into the flour using your fingertips until it resembles a crumbly texture. </li>
              <li>Gradually add warm water to the flour mixture, a little at a time, and knead the dough. Keep adding water and kneading until you have a smooth and pliable dough. </li>
              <li>Once the dough is well-kneaded, cover it with a damp cloth or plastic wrap and let it rest for about 15-20 minutes. </li>
              <li>After resting, divide the dough into small equal-sized balls, roughly the size of a golf ball.</li>
              <li>Take one dough ball and flatten it slightly with your palm. Dust it with some flour and roll it out into a thin, round disc using a rolling pin. Make sure to dust with flour as needed to prevent sticking.</li>
              <li>Heat a tawa (flat griddle) or a non-stick pan over medium-high heat.</li>
              <li>Place the rolled-out chapati onto the hot tawa and let it cook for about 30 seconds or until you see small bubbles forming on the surface.</li>
              <li>Flip the chapati and let the other side cook for another 30 seconds.</li>
              <li>Now, lift the chapati with a pair of tongs or a spatula and place it directly over an open flame (gas stove) or on an electric coil for a few seconds until it puffs up. Be careful not to burn it; move it around if needed to ensure even cooking.</li>
              <li>Remove the cooked chapati from the flame and transfer it to a plate. You can apply a small amount of ghee or butter on top if desired.</li>
              <li>Repeat the process with the remaining dough balls until all the chapatis are cooked.</li>
              <li>Serve the hot and freshly made chapatis with your favorite curries, vegetables, or chutneys.</li>
            </ol>
