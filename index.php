            <b>Ingredients</b>
          <ul>
            <li>2 cups idli rice (parboiled rice)</li>
            <li>1 cup urad dal (split black gram)</li>
            <li>1 teaspoon fenugreek seeds</li>
            <li>Salt to taste</li>
            <li>Water for soaking and grinding</li>
            <li>Cooking oil or ghee (clarified butter) for greasing</li>
          </ul><br>
  
          <b>Cooking Instructions</b>
          <ol style="text-align: justify;">
            <li> Wash the idli rice and urad dal separately and soak them in water overnight or for at least 4-6 hours. Also, soak the fenugreek seeds with the urad dal.</li>
            <li>Drain the water from the soaked rice and urad dal separately.</li>
            <li>In a blender or wet grinder, grind the urad dal and fenugreek seeds together to a smooth and fluffy consistency. Add water as needed while grinding. </li>
            <li>Transfer the urad dal batter to a large bowl. </li>
            <li>Now, grind the soaked rice to a slightly coarse texture. Add water as needed while grinding. </li>
            <li>Mix the rice batter with the urad dal batter in the bowl. Add salt to taste and mix well. The batter should have a slightly thick pouring consistency. </li>
            <li>Cover the bowl with a clean cloth or lid and let it ferment in a warm place for 8-10 hours or overnight. The batter will rise and become fluffy. </li>
            <li>Once the batter has fermented, give it a gentle stir. </li>
            <li>Grease the idli molds with cooking oil or ghee. </li>
            <li>Pour the batter into the greased molds, filling them about 3/4th full. </li>
            <li>Steam the idlis in a steamer for about 10-12 minutes or until a toothpick inserted in the center comes out clean.</li>
            <li>Remove the idli molds from the steamer and let them cool for a few minutes.</li>
            <li>Using a spoon or a knife, gently loosen the edges of the idlis and remove them from the molds.</li>
            <li>Serve the idlis hot with sambar, coconut chutney, and/or tomato chutney.</li>

