          <h2><u>Sweet and Sour Pork</u></h2><br>
          <b>Ingredients</b>
          <ul>
          <li>1 pound pork butt, cut into 1 inch cubes</li><br>
          <b>Marinade:</b>
          <ul>
              <li>1 teaspoon soy sauce</li>
              <li>1 teaspoon salt</li>
              <li>¼ teaspoon white sugar</li>
              <li>1 egg white</li>
              <li>2 green onions, chopped</li>
              </ul><br>

              <b>Frying:</b>
              <li>1 quart vegetable oil for frying</li>
              <li>½ cup cornstarch</li><br>

              <b>Stir-Fried Vegetables:</b>
              <li>1 tablespoon vegetable oil</li>
              <li>3 stalks celery, cut into 1/2 inch pieces</li>
              <li>1 medium green bell pepper, cut into 1 inch pieces</li>
              <li>1 medium onion, cut into wedges</li>
              <li>1 pinch white sugar, or to taste</li>
              <li>salt to taste</li>
              <li>1 ¼ cups water, divided</li>
              <li>¾ cup white sugar</li>
              <li>⅓ cup apple cider vinegar</li>
              <li>¼ cup ketchup</li>
              <li>½ teaspoon soy sauce</li>
              <li>¼ teaspoon salt</li>
              <li>1(8 ounce) can pineapple chunks, undrained</li>
              <li>2 tablespoons cornstarch</li>
              </ul><br>
              <b>Cooking Instructions</b>
              <ol style="text-align: justify;">
              <li>Place cubed pork in a medium bowl and season with soy sauce, salt, and sugar for marinade. Add egg white and green onions and mix until well-combined. Cover and place in the refrigerator for at least 1 hour. </li>
              <li>Heat 1 quart oil to 365 degrees F (185 degrees C) in a large, heavy saucepan or deep fryer. </li>
              <li>Coat pork with cornstarch and fry in hot oil until evenly browned, about 10 minutes. Drain on paper towels and set aside. </li>
              <li>Heat oil for vegetables in a wok over medium heat. Stir in celery, bell pepper, and onion; cook until tender, about 5 minutes. Season with pinch of sugar and salt. Remove from heat and set aside. </li>
              <li>Mix 1 cup water, 3/4 cup sugar, apple cider vinegar, ketchup, soy sauce, and salt in a large saucepan; bring to a boil over high heat. </li>
              <li>Stir in cooked pork, celery mixture, and pineapple chunks with juice. Return to a boil, then mix in remaining1/4 cup water and cornstarch. Cook until thickened and warmed through, about 2 minutes. </li>
              </ol> 
