          <h2><u>Ma Po Tofu</u></h2><br>
          
          <b>Main ingredients:</b>
          <ul>
           <li>500g fresh tofu, </li>
          <li>150g minced (scalped) beef,</li>
          <li>50g green onions
          </li>
        </ul><br>
          
          <b>Seasonings:</b>
          <ul>
          <li> 120g cooking oil,</li>
          <li> 5.5g salt,</li>
          <li> 20 ml Mapo tofu sauce (soy sauce),</li>
          <li> chili powder (to taste),</li>
          <li> 20g Pixian bean paste, </li>
          <li>250g double-boiled soup (made from bone with ginger and pepper),</li>
          <li> 40g starch and water (the starchy sauce), (2g chicken powder)</li>
          </ul><br>

          <b>Preparation</b>
          <ol style="text-align: justify;">
            <li>Dice the tofu into 2 cm (½–1") cubes, and put them into a bowl of hot water to remove astringency.</li>
            <li>Clean and cut the green onions into small pieces</li>
            <li>Make the starchy sauce by mixing the starch and water. </li><br>
          </ol>
            
          <b>Cooking Instructions</b>
          <ol style="text-align: justify;">
            <li> Heat up a wok with the prepared vegetable oil to around 80 °C. </li>
            <li> Fry the chopped beef in the wok till it becomes light brown, and then add the salt, Mapo tofu sauce, chili powder and Pixian bean paste. Keep frying. </li>
            <li> Add the double-boiled soup and diced tofu, and use a moderate heat to cook till tasty. </li>
            <li> Add the garlic sprouts and soy sauce, cook for a few seconds and thicken with the starchy sauce. (Add chicken powder if you like.) </li>
            <li>Serve</li>
          </ol>
